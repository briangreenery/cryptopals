fn xor(text: &[u8], key: &[u8]) -> Vec<u8> {
    text.iter().enumerate().map(|(index, item)| item ^ key[index % key.len()]).collect()
}

fn get_chunks(bytes: &[u8], size: usize) -> Vec<Vec<u8>> {
    let mut result = Vec::new();

    for _i in (0..size) {
        result.push(Vec::new());
    }

    for chunk in bytes.chunks(size) {
        for i in (0..chunk.len()) {
            result[i].push(chunk[i]);
        }
    }

    result
}

fn key_distance(size: usize, cipher: &[u8]) -> f64 {
    let mut total = 0;

    for i in (0..4) {
        let a = &cipher[(i * size)..((i + 1) * size)];
        let b = &cipher[((i + 1) * size)..((i + 2) * size)];

        total += pals::hamming_distance(a, b);
    }

    (total as f64) / (size as f64)
}

fn guess_key_sizes(cipher: &[u8]) -> Vec<usize> {
    let mut guesses: Vec<usize> = (2..40).collect();

    guesses.sort_by(|a, b| {
        let a_dist = key_distance(*a, cipher);
        let b_dist = key_distance(*b, cipher);

        if a_dist < b_dist {
            std::cmp::Ordering::Less
        } else if a_dist > b_dist {
            std::cmp::Ordering::Greater
        } else {
            std::cmp::Ordering::Equal
        }
    });

    guesses
}

fn score(letters: &[u8]) -> i32 {
    let mut total = 0;

    for letter in letters.iter() {
        match *letter {
            b'a'...b'z' => total += 10,
            b'A'...b'Z' => total += 2,
            b'0'...b'9' => total += 2,
            b' ' => total += 2,
            b'\n' => total += 1,
            b'\r' => total += 1,
            b'!' => total += 1,
            b'.' => total += 1,
            b'?' => total += 1,
            0...20 => total -= 20,
            128...255 => total -= 20,
            _ => total -= 5,
        }
    }

    total
}

fn guess_key_byte(chunk: &[u8]) -> u8 {
    let mut best_score: i32 = 0;
    let mut best_byte: u8 = 0;

    for byte in (0..255) {
        let text = xor(&chunk, &[byte]);
        let score = score(&text);

        if score > best_score {
            best_score = score;
            best_byte = byte;
        }
    }

    best_byte
}

fn main() {
    let cipher = test_data();
    let guesses = guess_key_sizes(&cipher);

    let mut best_score = 0;
    let mut best_key: Vec<u8> = Vec::new();

    for size in guesses.iter().take(10) {
        println!("Trying key size {}.", size);

        let key: Vec<u8> = get_chunks(&cipher, *size)
                               .iter()
                               .map(|chunk| guess_key_byte(chunk))
                               .collect();
        let decoded = xor(&cipher, &key);
        let score = score(&decoded);

        if score > best_score {
            best_score = score;
            best_key = key;
        }
    }

    println!("key: \"{}\"", String::from_utf8(best_key.clone()).unwrap());
    println!("{}", String::from_utf8(xor(&cipher, &best_key)).unwrap());
}
